package chococraft.common.carrier;

public class CarrierNode
{
	public final int nodeId;
	public final int posX;
	public final int posY;
	public final int posZ;
	
	public CarrierNode(int nodeId, int posX, int posY, int posZ)
	{
		this.nodeId = nodeId;
		this.posX = posX;
		this.posY = posY;
		this.posZ = posZ;
	}
}
